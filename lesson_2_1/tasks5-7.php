<h3>6. Немного изменим п.5. Объясните результаты в этом случае.</h3>
<?php

class A {
  public function foo() {
  static $x = 0;
  echo ++$x;
  }
}
class B extends A {
  // при наследовании метод родительского класса переопределяется,
  // поэтому при обращении к методам foo() класса A и B по сути
  // происходит обращение к разным функциям, в каждой из которых
  // своя статичная переменная $x
}
$a1 = new A();
$b1 = new B();
$a1->foo(); //1
$b1->foo(); //1
$a1->foo(); //2
$b1->foo(); //2
?>


